import { withSentryConfig } from "@sentry/nextjs";
import type { NextConfig } from "next";

const apiProxyTarget = process.env.API_PROXY_TARGET?.replace(/\/$/, "");
const isDev = process.env.NODE_ENV === "development";

const nextConfig: NextConfig = {
  images: {
    remotePatterns: [
      {
        protocol: "https",
        hostname: "res.cloudinary.com",
      },
    ],
  },
  async headers() {
    return [
      {
        source: "/(.*)",
        headers: [
          { key: "X-Frame-Options", value: "DENY" },
          { key: "X-Content-Type-Options", value: "nosniff" },
          { key: "Referrer-Policy", value: "strict-origin-when-cross-origin" },
          {
            key: "Permissions-Policy",
            value: "camera=(self), microphone=(self), geolocation=()",
          },
          {
            key: "Strict-Transport-Security",
            value: "max-age=63072000; includeSubDomains; preload",
          },
          {
            key: "Content-Security-Policy",
            value: [
              "default-src 'self'",
              `script-src 'self' 'unsafe-inline'${isDev ? " 'unsafe-eval'" : ""} https://*.razorpay.com https://static.cloudflareinsights.com https://challenges.cloudflare.com`,
              "style-src 'self' 'unsafe-inline'",
              "img-src 'self' data: blob: https://res.cloudinary.com",
              "font-src 'self'",
              "connect-src 'self' https://*.razorpay.com https://static.cloudflareinsights.com wss://*.livekit.cloud https://*.livekit.cloud" +
                // Only append NEXT_PUBLIC_API_URL when it's an absolute URL.
                // Relative paths like "/api/v1" aren't valid CSP sources and
                // get ignored by the browser (with a console warning).
                (process.env.NEXT_PUBLIC_API_URL?.startsWith("http")
                  ? ` ${process.env.NEXT_PUBLIC_API_URL}`
                  : ""),
              "media-src 'self' blob:",
              "frame-src 'self' blob: https://*.razorpay.com https://challenges.cloudflare.com",
              "object-src 'self'",
              "base-uri 'self'",
              "form-action 'self'",
            ].join("; "),
          },
        ],
      },
    ];
  },
  async rewrites() {
    if (!apiProxyTarget) {
      return [];
    }

    return [
      {
        source: "/api/v1/:path*",
        destination: `${apiProxyTarget}/api/v1/:path*`,
      },
    ];
  },
};

